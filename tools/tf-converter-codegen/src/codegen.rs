//! Emit `generated/<service>.rs` from a parsed `ServiceSpec`.
//!
//! The output is a serde-driven `TfModel` struct per resource, plus
//! `into_state_view(self, ctx, region) -> {View}` and
//! `From<&{View}> for {Model}` impls. Decode and encode are pure
//! `serde_json::from_value` / `serde_json::to_value` at call sites — this
//! module only emits the struct definition and the projection bridges.

use std::collections::BTreeSet;
use std::fmt::Write;

use anyhow::{Result, bail};
use heck::ToSnakeCase;

use crate::spec::{
    ComputedExtract, Field, Resource, Service, ServiceSpec, TemplatedField, ViewExtra,
};

pub fn render(spec: &ServiceSpec) -> Result<String> {
    let mut s = String::new();

    writeln!(
        s,
        "//! Auto-generated by tf-converter-codegen. Do not edit manually."
    )
    .unwrap();
    writeln!(s, "//! Source: specs/{}.toml", spec.service.name).unwrap();
    writeln!(
        s,
        "//! Regenerate with: cargo run -p tf-converter-codegen -- gen {}",
        spec.service.name
    )
    .unwrap();
    writeln!(s).unwrap();
    let needs_hashmap = spec.resources.iter().any(|r| {
        r.fields.iter().any(|f| f.ty == "tags")
            || r.computed_extract
                .iter()
                .any(|ce| ce.ty.as_deref() == Some("tags"))
    });
    if needs_hashmap {
        writeln!(s, "use std::collections::HashMap;").unwrap();
        writeln!(s).unwrap();
    }
    writeln!(s, "use serde::{{Deserialize, Serialize}};").unwrap();

    let any_projecting = spec.resources.iter().any(|r| r.modes.mode != "model_only");
    if any_projecting {
        writeln!(s).unwrap();
        writeln!(s, "use crate::converter::ConversionContext;").unwrap();
        let mut view_names: BTreeSet<&str> = BTreeSet::new();
        for r in &spec.resources {
            if r.modes.mode != "model_only" {
                view_names.insert(r.view.as_str());
            }
        }
        for view in &view_names {
            writeln!(
                s,
                "use {}::{}::{};",
                spec.service.crate_name, spec.service.state_view_module, view
            )
            .unwrap();
        }
    }
    writeln!(s).unwrap();

    for resource in &spec.resources {
        render_resource(&mut s, &spec.service, resource)?;
    }
    Ok(s)
}

fn render_resource(s: &mut String, _service: &Service, r: &Resource) -> Result<()> {
    render_struct(s, r)?;
    writeln!(s).unwrap();
    render_default_fns(s, r)?;
    if r.modes.mode != "model_only" {
        render_into_state_view(s, r)?;
        writeln!(s).unwrap();
        render_from_state_view(s, r)?;
        writeln!(s).unwrap();
    }
    Ok(())
}

/// Strip the `TfModel` suffix from `r.model` if present, then snake_case the
/// rest. Used as a prefix for generated default-value functions to avoid
/// cross-resource collisions in services with multiple resources.
fn model_slug(r: &Resource) -> String {
    let trimmed = r.model.strip_suffix("TfModel").unwrap_or(r.model.as_str());
    trimmed.to_snake_case()
}

fn render_struct(s: &mut String, r: &Resource) -> Result<()> {
    writeln!(
        s,
        "/// Auto-generated TF-shaped projection of the `{}` resource.",
        r.resource_type
    )
    .unwrap();
    writeln!(s, "#[derive(Debug, Clone, Serialize, Deserialize)]").unwrap();
    writeln!(s, "pub struct {} {{", r.model).unwrap();

    for f in &r.fields {
        render_field(s, r, f)?;
    }
    if let Some(arn) = &r.computed.arn {
        render_computed_template_field(s, "arn", arn);
    }
    if let Some(url) = &r.computed.url {
        render_computed_template_field(s, "url", url);
    }
    for ce in &r.computed_extract {
        // Skip a computed_extract entry whose `hcl` collides with a
        // template field already emitted (`arn`, `url`) or a regular
        // resource.field. The struct only carries one definition per
        // logical attribute name.
        if computed_extract_collides(r, ce) {
            continue;
        }
        render_computed_extract_field(s, ce)?;
    }
    writeln!(s, "}}").unwrap();
    Ok(())
}

fn render_field(s: &mut String, r: &Resource, f: &Field) -> Result<()> {
    let rust_type = field_rust_type(&f.ty)?;
    let renamed = f.hcl != f.view;
    let mut attrs: Vec<String> = Vec::new();
    if renamed {
        attrs.push(format!("rename = \"{}\"", f.hcl));
    }
    if !f.required {
        if f.default.is_some() {
            let fn_name = default_fn_name(r, f);
            attrs.push(format!("default = \"{fn_name}\""));
        } else {
            attrs.push("default".to_string());
        }
    }
    if rust_type.starts_with("Option<") {
        attrs.push("skip_serializing_if = \"Option::is_none\"".to_string());
    }
    if !attrs.is_empty() {
        writeln!(s, "    #[serde({})]", attrs.join(", ")).unwrap();
    }
    writeln!(s, "    pub {}: {},", escape_keyword(&f.view), rust_type).unwrap();
    Ok(())
}

/// Wrap Rust reserved keywords with the `r#` raw-identifier prefix so they
/// can appear as field names. Spec authors often use `type`, `move`, etc.
/// directly because those are valid HCL attribute names.
fn escape_keyword(name: &str) -> String {
    const KEYWORDS: &[&str] = &[
        "abstract", "as", "async", "await", "become", "box", "break", "const", "continue", "crate",
        "do", "dyn", "else", "enum", "extern", "false", "final", "fn", "for", "gen", "if", "impl",
        "in", "let", "loop", "macro", "match", "mod", "move", "mut", "override", "priv", "pub",
        "ref", "return", "self", "static", "struct", "super", "trait", "true", "try", "type",
        "typeof", "unsafe", "unsized", "use", "virtual", "where", "while", "yield",
    ];
    if KEYWORDS.contains(&name) {
        format!("r#{name}")
    } else {
        name.to_string()
    }
}

fn render_computed_template_field(s: &mut String, name: &str, _t: &TemplatedField) {
    writeln!(
        s,
        "    #[serde(default, skip_serializing_if = \"Option::is_none\")]"
    )
    .unwrap();
    writeln!(s, "    pub {name}: Option<String>,").unwrap();
}

fn render_computed_extract_field(s: &mut String, ce: &ComputedExtract) -> Result<()> {
    let inner = computed_extract_rust_type(ce)?;
    let renamed = ce.hcl_field_name() != ce.hcl;
    let mut attrs: Vec<String> = Vec::new();
    if renamed {
        attrs.push(format!("rename = \"{}\"", ce.hcl));
    }
    attrs.push("default".to_string());
    attrs.push("skip_serializing_if = \"Option::is_none\"".to_string());
    writeln!(s, "    #[serde({})]", attrs.join(", ")).unwrap();
    writeln!(
        s,
        "    pub {}: Option<{}>,",
        escape_keyword(&ce.hcl_field_name()),
        inner
    )
    .unwrap();
    Ok(())
}

fn computed_extract_collides(r: &Resource, ce: &ComputedExtract) -> bool {
    if r.computed.arn.is_some() && ce.hcl == "arn" {
        return true;
    }
    if r.computed.url.is_some() && ce.hcl == "url" {
        return true;
    }
    r.fields.iter().any(|f| f.hcl == ce.hcl || f.view == ce.hcl)
}

impl ComputedExtract {
    /// Field identifier on the model. Defaults to `hcl` (assumed to be a
    /// valid Rust identifier; if the spec ever needs a Rust-incompatible
    /// HCL name the spec format will need a separate `view` key).
    fn hcl_field_name(&self) -> String {
        self.hcl.clone()
    }
}

fn field_rust_type(ty: &str) -> Result<String> {
    Ok(match ty {
        "string" => "String".to_string(),
        "string?" => "Option<String>".to_string(),
        "u32" => "u32".to_string(),
        "i64" => "i64".to_string(),
        "bool" => "bool".to_string(),
        "tags" => "HashMap<String, String>".to_string(),
        other => bail!("unsupported field type: {other}"),
    })
}

fn computed_extract_rust_type(ce: &ComputedExtract) -> Result<String> {
    let raw = ce.ty.as_deref().unwrap_or("string");
    Ok(match raw {
        "string" | "string?" => "String".to_string(),
        "u32" => "u32".to_string(),
        "i64" => "i64".to_string(),
        "bool" => "bool".to_string(),
        "tags" => "HashMap<String, String>".to_string(),
        other => bail!("unsupported computed_extract type: {other}"),
    })
}

fn render_default_fns(s: &mut String, r: &Resource) -> Result<()> {
    for f in &r.fields {
        if f.required {
            continue;
        }
        let Some(default) = &f.default else { continue };
        let fn_name = default_fn_name(r, f);
        let rust_type = field_rust_type(&f.ty)?;
        let literal = render_default_literal(&f.ty, default)?;
        writeln!(s, "fn {fn_name}() -> {rust_type} {{").unwrap();
        writeln!(s, "    {literal}").unwrap();
        writeln!(s, "}}").unwrap();
        writeln!(s).unwrap();
    }
    Ok(())
}

fn default_fn_name(r: &Resource, f: &Field) -> String {
    format!("default_{}_{}", model_slug(r), f.hcl)
}

fn render_default_literal(ty: &str, value: &toml::Value) -> Result<String> {
    Ok(match (ty, value) {
        ("u32", toml::Value::Integer(n)) => format!("{n}u32"),
        ("i64", toml::Value::Integer(n)) => format!("{n}i64"),
        ("bool", toml::Value::Boolean(b)) => b.to_string(),
        ("string", toml::Value::String(s)) => format!("{:?}.to_string()", s),
        ("string?", toml::Value::String(s)) => format!("Some({:?}.to_string())", s),
        (t, v) => bail!("unsupported default literal: type={t} value={v:?}"),
    })
}

fn render_into_state_view(s: &mut String, r: &Resource) -> Result<()> {
    writeln!(s, "impl {} {{", r.model).unwrap();
    writeln!(
        s,
        "    /// Project the TF-shaped model into the AWS-side `{}`. Templates fire here",
        r.view
    )
    .unwrap();
    writeln!(s, "    /// because they need `ctx` for account and region.").unwrap();
    writeln!(
        s,
        "    pub fn into_state_view(self, ctx: &ConversionContext, region: &str) -> {} {{",
        r.view
    )
    .unwrap();

    let needs_name = r.computed.arn.is_some() || r.computed.url.is_some();
    let identifier_field = identifier_field_name(r);
    if needs_name {
        writeln!(
            s,
            "        let identifier = self.{}.clone();",
            identifier_field
        )
        .unwrap();
    }

    if let Some(arn) = &r.computed.arn {
        emit_template_resolution(s, "arn", arn, r);
    }
    if let Some(url) = &r.computed.url {
        emit_template_resolution(s, "url", url, r);
    }

    writeln!(s, "        {} {{", r.view).unwrap();

    // 1. Plain field assignments, by view name.
    for f in &r.fields {
        match f.ty.as_str() {
            "string" | "tags" | "u32" | "i64" | "bool" => {
                writeln!(s, "            {0}: self.{0},", f.view).unwrap();
            }
            "string?" => {
                writeln!(s, "            {0}: self.{0},", f.view).unwrap();
            }
            _ => bail!("unsupported field type during view assignment: {}", f.ty),
        }
    }
    if r.computed.arn.is_some() {
        writeln!(s, "            arn,").unwrap();
    }
    if r.computed.url.is_some() {
        writeln!(s, "            url,").unwrap();
    }

    // 2. View extras: fields on the StateView with no model counterpart.
    for ve in &r.view_extras {
        emit_view_extra_assignment(s, ve);
    }

    writeln!(s, "        }}").unwrap();
    writeln!(s, "    }}").unwrap();
    writeln!(s, "}}").unwrap();
    Ok(())
}

fn identifier_field_name(r: &Resource) -> String {
    // Convention: the first field marked `required = true` is treated as
    // the resource identifier and is the placeholder substituted for
    // `{name}` in ARN/URL templates. If no required field is declared,
    // fall back to a field literally named `name`.
    if let Some(f) = r.fields.iter().find(|f| f.required) {
        return f.view.clone();
    }
    if let Some(f) = r.fields.iter().find(|f| f.view == "name") {
        return f.view.clone();
    }
    "name".to_string()
}

fn emit_template_resolution(s: &mut String, name: &str, t: &TemplatedField, r: &Resource) {
    let template_call = build_template_call(&t.template, r);
    let chain: Vec<String> = if let Some(over) = &t.attr_override {
        vec![format!("self.{over}.clone()")]
    } else {
        t.attr_override_chain
            .iter()
            .map(|attr| format!("self.{attr}.clone()"))
            .collect()
    };
    if chain.is_empty() {
        writeln!(s, "        let {name} = {template_call};").unwrap();
        return;
    }
    let head = &chain[0];
    let tail = &chain[1..];
    write!(s, "        let {name} = {head}").unwrap();
    for next in tail {
        write!(s, "\n            .or({next})").unwrap();
    }
    writeln!(s, "\n            .unwrap_or_else(|| {template_call});").unwrap();
}

fn build_template_call(template: &str, r: &Resource) -> String {
    // Replace {placeholder} with positional `{}` and collect the args.
    let mut format_str = String::new();
    let mut args: Vec<String> = Vec::new();
    let mut chars = template.chars().peekable();
    while let Some(c) = chars.next() {
        if c == '{' {
            let mut name = String::new();
            for c2 in chars.by_ref() {
                if c2 == '}' {
                    break;
                }
                name.push(c2);
            }
            format_str.push_str("{}");
            args.push(template_arg(&name, r));
        } else {
            format_str.push(c);
        }
    }
    let joined = args.join(", ");
    format!("format!({:?}, {})", format_str, joined)
}

fn template_arg(placeholder: &str, r: &Resource) -> String {
    match placeholder {
        "region" => "region".to_string(),
        "account" | "account_id" => "ctx.default_account_id".to_string(),
        "name" => "identifier".to_string(),
        other => {
            // Allow direct field references; the codegen doesn't validate
            // these against the spec at emit time, so users get a Rust
            // compiler error if they typo.
            let _ = r;
            format!("self.{other}")
        }
    }
}

fn emit_view_extra_assignment(s: &mut String, ve: &ViewExtra) {
    match ve.source.as_str() {
        "region" => {
            writeln!(s, "            {}: region.to_string(),", ve.view).unwrap();
        }
        "account_id" => {
            writeln!(
                s,
                "            {}: ctx.default_account_id.clone(),",
                ve.view
            )
            .unwrap();
        }
        "none" => {
            writeln!(s, "            {}: None,", ve.view).unwrap();
        }
        "default" => {
            writeln!(
                s,
                "            {}: ::core::default::Default::default(),",
                ve.view
            )
            .unwrap();
        }
        other => {
            // Best-effort: emit a literal expression so reviewers see the
            // unrecognised source and the build fails loudly.
            writeln!(
                s,
                "            {}: /* unknown view_extra source: {} */ Default::default(),",
                ve.view, other
            )
            .unwrap();
        }
    }
}

fn render_from_state_view(s: &mut String, r: &Resource) -> Result<()> {
    writeln!(s, "impl From<&{}> for {} {{", r.view, r.model).unwrap();
    writeln!(s, "    fn from(v: &{}) -> Self {{", r.view).unwrap();
    writeln!(s, "        Self {{").unwrap();
    for f in &r.fields {
        match f.ty.as_str() {
            "string" => writeln!(s, "            {0}: v.{0}.clone(),", f.view).unwrap(),
            "string?" => writeln!(s, "            {0}: v.{0}.clone(),", f.view).unwrap(),
            "tags" => writeln!(s, "            {0}: v.{0}.clone(),", f.view).unwrap(),
            "u32" | "i64" | "bool" => writeln!(s, "            {0}: v.{0},", f.view).unwrap(),
            other => bail!("unsupported field type during From<&View>: {other}"),
        }
    }
    if r.computed.arn.is_some() {
        writeln!(s, "            arn: Some(v.arn.clone()),").unwrap();
    }
    if r.computed.url.is_some() {
        writeln!(s, "            url: Some(v.url.clone()),").unwrap();
    }
    for ce in &r.computed_extract {
        if computed_extract_collides(r, ce) {
            continue;
        }
        let raw = ce.ty.as_deref().unwrap_or("string");
        match raw {
            "string" | "string?" => writeln!(
                s,
                "            {}: Some(v.{}.clone()),",
                ce.hcl_field_name(),
                ce.source
            )
            .unwrap(),
            "tags" => writeln!(
                s,
                "            {}: Some(v.{}.clone()),",
                ce.hcl_field_name(),
                ce.source
            )
            .unwrap(),
            "u32" | "i64" | "bool" => writeln!(
                s,
                "            {}: Some(v.{}),",
                ce.hcl_field_name(),
                ce.source
            )
            .unwrap(),
            other => bail!("unsupported computed_extract type: {other}"),
        }
    }
    writeln!(s, "        }}").unwrap();
    writeln!(s, "    }}").unwrap();
    writeln!(s, "}}").unwrap();
    Ok(())
}
